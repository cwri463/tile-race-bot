CHECK_EMOJI = "\N{WHITE HEAVY CHECK MARK}"   # âœ…
CROSS_EMOJI = "\N{CROSS MARK}"               # âŒ
FORK_EMOJIS = ["ðŸ‡¦", "ðŸ‡§", "ðŸ‡¨", "ðŸ‡©", "ðŸ‡ª", "ðŸ‡«"]

